package g44422.diamond.model;

/**
 * The different states of an explorer.
 *
 * @author 44422
 * @version 0.1
 */
public enum State {
    /**
     * Value of an exploring explorer.
     */
    EXPLORING,
    /**
     * Value of an leaving explorer.
     */
    LEAVING,
    /**
     * Value of an explorer at his camp.
     */
    CAMPING
}
